def gringify(aString, asm=False):
	#example return for asm=False (implies we're using EA to insert): BYTE 0x82 0xE5 0x00
	#example return for asm=True: .byte 0x82, 0xE5, 0x00
	
	returnString = ''
	
	if asm==True:
		returnString += '.byte '
		for letter in aString:
			try:
				returnString += str(hex(gringeDict[letter][0])) + ', ' + str(hex(gringeDict[letter][1]))+ ', '
			except:
				print('Character ' + letter + ' not in dictionary!')
		returnString += '0x00'
	else:
		returnString += 'BYTE '
		for letter in aString:
			try:
				returnString += str(hex(gringeDict[letter][0])) + ' ' + str(hex(gringeDict[letter][1]))+ ' '
			except:
				print('Character ' + letter + ' not in dictionary!')
		returnString += '0x00'
	return returnString

